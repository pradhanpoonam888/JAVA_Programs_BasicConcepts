package Calculator;

public class MainCalculator {
	public int x;
	public int y;
	
	public int add(int x,int y) {
		return x+y;
	}
	public int subtract(int x,int y) {
		return x-y;
	}
	public int multiply(int x,int y) {
		return x*y;
	}
	public int divide(int x, int y) {
		return x/y;
	}
	public int modulus(int x,int y) {
		return x%y;
	}

}
